"""
Пример 1.1: Работа с переменными и типами данных
Демонстрирует основные типы данных Python
"""

# Числовые переменные
параметр_расчета = 10
напряжение_сети = 220.5
количество_итераций = 5

# Строковые переменные
название_проекта = "Проект_2024"
путь_к_файлу = r"C:\RasterWin\Проекты\project.rst"  # r перед строкой - raw string
тип_режима = 'установившийся'

# Булевы переменные
расчет_завершен = True
есть_ошибки = False
режим_устойчив = True

# Вывод информации о переменных
print("=" * 50)
print("ИНФОРМАЦИЯ О ПЕРЕМЕННЫХ")
print("=" * 50)

print(f"Параметр расчета: {параметр_расчета} (тип: {type(параметр_расчета).__name__})")
print(f"Напряжение сети: {напряжение_сети} В (тип: {type(напряжение_сети).__name__})")
print(f"Название проекта: {название_проекта} (тип: {type(название_проекта).__name__})")
