"""
ЗАДАНИЕ 1.2: Работа со списками

ЗАДАЧА:
Работа с параметрами нагрузки для последовательных расчетов.

ИСХОДНЫЕ ДАННЫЕ:
параметры_нагрузки = [100, 120, 140, 160, 180, 200, 220, 240]

ЗАДАНИЯ:
1. Выведите первый, последний и средний элементы списка
2. Добавьте в список значения 260 и 280
3. Вставьте значение 110 после первого элемента
4. Найдите максимальное и минимальное значение в списке (без использования max/min)
5. Создайте новый список только с параметрами больше 150
6. Вычислите среднее значение всех параметров
7. Выведите все параметры с их индексами

ПОДСКАЗКИ:
- Используйте цикл for с enumerate() для получения индексов
- Для вычисления среднего: сумма / количество
- Для поиска максимума/минимума используйте цикл

"""

# ===== ИСХОДНЫЕ ДАННЫЕ =====
параметры_нагрузки = [100, 120, 140, 160, 180, 200, 220, 240]

# ===== ВАШ КОД НИЖЕ =====

# 1. Выведите первый, последний и средний элементы
print("первый элемент: ", параметры_нагрузки[0])
print("последний элемент: ", параметры_нагрузки[-1])
print("среднее значение: ", параметры_нагрузки[len(параметры_нагрузки) // 2])



# 2. Добавьте значения 260 и 280
параметры_нагрузки.append(260)
параметры_нагрузки.append(280)
print("после добавления 260 и 280: ", параметры_нагрузки)


# 3. Вставьте 110 после первого элемента
параметры_нагрузки.insert(1, 110)
print("после вставки 110 после первого элемента: ", параметры_нагрузки)

# 4. Найдите максимальное значение
максимальное_значение = параметры_нагрузки[0]
for параметр in параметры_нагрузки:
    if параметр > максимальное_значение:
        максимальное_значение = параметр
print("максимальное значение: ", максимальное_значение)


# 5. Найдите минимальное значение
минимальное_значение = параметры_нагрузки[0]
for параметр in параметры_нагрузки:
    if параметр < минимальное_значение:
        минимальное_значение = параметр
print("минимальное значение: ", минимальное_значение)

# 6. Создайте список параметров > 150
большие_параметры = []
for параметр in параметры_нагрузки:
    if параметр > 150:
        большие_параметры.append(параметр)
print("большие параметры: ", большие_параметры)

# 7. Вычислите среднее значение
среднее_значение = sum(параметры_нагрузки) / len(параметры_нагрузки)
print("среднее значение: ", среднее_значение)
# 8. Выведите все параметры с индексами
for индекс, параметр in enumerate(параметры_нагрузки):
    print("индекс: ", индекс, "значение: ", параметр)

