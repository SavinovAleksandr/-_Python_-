"""
РЕШЕНИЕ ЗАДАНИЯ 1.1: Работа с переменными и типами данных
"""

print("=" * 60)
print("РЕШЕНИЕ ЗАДАНИЯ 1.1")
print("=" * 60)

# 1. Создание переменных
название_проекта = "Проект_2024"
начальное_напряжение = 220.0
количество_итераций = 10
расчет_завершен = False
допустимое_отклонение = 20.0

# 2. Вывод типов переменных
print("\n1. ТИПЫ ПЕРЕМЕННЫХ:")
print("-" * 60)
print(f"название_проекта = '{название_проекта}' (тип: {type(название_проекта).__name__})")
print(f"начальное_напряжение = {начальное_напряжение} В (тип: {type(начальное_напряжение).__name__})")
print(f"количество_итераций = {количество_итераций} (тип: {type(количество_итераций).__name__})")
print(f"расчет_завершен = {расчет_завершен} (тип: {type(расчет_завершен).__name__})")
print(f"допустимое_отклонение = {допустимое_отклонение} В (тип: {type(допустимое_отклонение).__name__})")

# 3. Изменение напряжения (увеличение на 10%)
новое_напряжение = начальное_напряжение * 1.1  # или начальное_напряжение + начальное_напряжение * 0.1
print(f"\n2. ИЗМЕНЕНИЕ НАПРЯЖЕНИЯ:")
print("-" * 60)
print(f"Начальное напряжение: {начальное_напряжение} В")
print(f"Новое напряжение (увеличение на 10%): {новое_напряжение} В")

# 4. Создание строки с полной информацией
полная_информация = (
    f"Проект: {название_проекта}, "
    f"Начальное напряжение: {начальное_напряжение} В, "
    f"Итераций: {количество_итераций}, "
    f"Расчет завершен: {расчет_завершен}, "
    f"Допустимое отклонение: {допустимое_отклонение} В"
)
print(f"\n3. ПОЛНАЯ ИНФОРМАЦИЯ:")
print("-" * 60)
print(полная_информация)

# 5. Проверка диапазона напряжения
мин_напряжение = начальное_напряжение - допустимое_отклонение
макс_напряжение = начальное_напряжение + допустимое_отклонение
в_диапазоне = мин_напряжение <= новое_напряжение <= макс_напряжение

print(f"\n4. ПРОВЕРКА ДИАПАЗОНА НАПРЯЖЕНИЯ:")
print("-" * 60)
print(f"Минимальное допустимое: {мин_напряжение} В")
print(f"Максимальное допустимое: {макс_напряжение} В")
print(f"Новое напряжение: {новое_напряжение} В")
print(f"Находится в диапазоне: {в_диапазоне}")

if в_диапазоне:
    print("✓ Напряжение в допустимых пределах")
else:
    print("✗ Напряжение выходит за допустимые пределы!")

print("\n" + "=" * 60)

