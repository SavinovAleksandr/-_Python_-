"""
РЕШЕНИЕ ЗАДАНИЯ 1.2: Работа со списками
"""

print("=" * 60)
print("РЕШЕНИЕ ЗАДАНИЯ 1.2")
print("=" * 60)

# Исходные данные
параметры_нагрузки = [100, 120, 140, 160, 180, 200, 220, 240]
print(f"\nИсходный список: {параметры_нагрузки}")

# 1. Вывод первого, последнего и среднего элементов
print("\n1. ПЕРВЫЕ/ПОСЛЕДНИЕ/СРЕДНИЙ ЭЛЕМЕНТЫ:")
print("-" * 60)
первый = параметры_нагрузки[0]
последний = параметры_нагрузки[-1]
средний_индекс = len(параметры_нагрузки) // 2
средний = параметры_нагрузки[средний_индекс]

print(f"Первый элемент: {первый}")
print(f"Последний элемент: {последний}")
print(f"Средний элемент (индекс {средний_индекс}): {средний}")

# 2. Добавление значений 260 и 280
print("\n2. ДОБАВЛЕНИЕ ЭЛЕМЕНТОВ:")
print("-" * 60)
параметры_нагрузки.append(260)
параметры_нагрузки.append(280)
print(f"После добавления 260 и 280: {параметры_нагрузки}")

# 3. Вставка значения 110 после первого элемента
print("\n3. ВСТАВКА ЭЛЕМЕНТА:")
print("-" * 60)
параметры_нагрузки.insert(1, 110)  # индекс 1 = после первого элемента (индекс 0)
print(f"После вставки 110 на позицию 1: {параметры_нагрузки}")

# 4. Поиск максимального значения
print("\n4. ПОИСК МАКСИМАЛЬНОГО ЗНАЧЕНИЯ:")
print("-" * 60)
максимум = параметры_нагрузки[0]
индекс_максимума = 0

for индекс, значение in enumerate(параметры_нагрузки):
    if значение > максимум:
        максимум = значение
        индекс_максимума = индекс

print(f"Максимальное значение: {максимум} (индекс {индекс_максимума})")

# 5. Поиск минимального значения
print("\n5. ПОИСК МИНИМАЛЬНОГО ЗНАЧЕНИЯ:")
print("-" * 60)
минимум = параметры_нагрузки[0]
индекс_минимума = 0

for индекс, значение in enumerate(параметры_нагрузки):
    if значение < минимум:
        минимум = значение
        индекс_минимума = индекс

print(f"Минимальное значение: {минимум} (индекс {индекс_минимума})")

# 6. Создание списка параметров > 150
print("\n6. ПАРАМЕТРЫ БОЛЬШЕ 150:")
print("-" * 60)
большие_параметры = []
for параметр in параметры_нагрузки:
    if параметр > 150:
        большие_параметры.append(параметр)

# Альтернативный способ (list comprehension):
# большие_параметры = [п for п in параметры_нагрузки if п > 150]

print(f"Параметры больше 150: {большие_параметры}")

# 7. Вычисление среднего значения
print("\n7. СРЕДНЕЕ ЗНАЧЕНИЕ:")
print("-" * 60)
сумма = 0
for параметр in параметры_нагрузки:
    сумма += параметр

среднее_значение = сумма / len(параметры_нагрузки)
print(f"Сумма всех параметров: {сумма}")
print(f"Количество элементов: {len(параметры_нагрузки)}")
print(f"Среднее значение: {среднее_значение:.2f}")

# 8. Вывод всех параметров с индексами
print("\n8. ВСЕ ПАРАМЕТРЫ С ИНДЕКСАМИ:")
print("-" * 60)
for индекс, параметр in enumerate(параметры_нагрузки):
    print(f"  Индекс {индекс}: {параметр}")

print("\n" + "=" * 60)

