# 2. Функция проверить_устойчивость
def проверить_устойчивость(напряжение, минимальное_напряжение=200):
    """
    Проверяет, находится ли напряжение выше минимального значения

    Аргументы:
        напряжение (float): Проверяемое напряжение
        минимальное_напряжение (float): Минимальное допустимое значение (по умолчанию 200)

    Возвращает:
        bool: True если напряжение >= минимальное_напряжение, иначе False
    """
    try:
        return float(напряжение) >= float(минимальное_напряжение)
    except (TypeError, ValueError):
        return False


